def transaction_list(request):
    transactions = (
        Transaction.objects.filter(is_active=True)
        .select_related("company_account")
        .order_by("-tx_date", "-id")
    )

    return render(
        request,
        "transactions/transaction_list.html",
        {"transactions": transactions,
         "segment": "transactions",},
    )



